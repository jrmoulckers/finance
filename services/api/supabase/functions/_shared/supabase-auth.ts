function authUrl(): string {
  return `${requireEnv('SUPABASE_URL')}/auth/v1`;
}

type TokenRequestBody = Record<string, string>;

async function goTrueTokenRequest(
  grantType: 'password' | 'refresh_token' | 'pkce',
  body: TokenRequestBody,
): Promise<SupabaseTokenResponse | null> {
  const url = `${authUrl()}/token?grant_type=${grantType}`;
  const response = await fetch(url, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      apikey: requireEnv('SUPABASE_ANON_KEY'),
    },
    body: JSON.stringify(body),
  });
  if (!response.ok) return null;
  const json = (await response.json()) as Partial<SupabaseTokenResponse>;
  if (
    typeof json.access_token !== 'string' ||
    typeof json.refresh_token !== 'string' ||
    typeof json.expires_in !== 'number'
  ) {
    return null;
  }
  return {
    access_token: json.access_token,
    refresh_token: json.refresh_token,
    expires_in: json.expires_in,
    token_type: 'bearer',
  };
}

const INVALID_REFRESH_TOKEN_CODES = new Set([
  'refresh_token_already_used',
  'refresh_token_not_found',
  'session_expired',
  'session_not_found',
]);

const TRANSIENT_REFRESH_ERROR_CODES = new Set([
  'conflict',
  'over_request_rate_limit',
  'request_timeout',
  'unexpected_failure',
]);

async function readSupabaseAuthError(response: Response): Promise<SupabaseAuthError> {
  const text = await response.text();
  const parsed = parseJsonObject(text);
  const code = getStringField(parsed, 'code') ?? getStringField(parsed, 'error_code');
  const error = getStringField(parsed, 'error');
  const message =
    getStringField(parsed, 'msg') ??
    getStringField(parsed, 'message') ??
    getStringField(parsed, 'error_description') ??
    (text.trim().length > 0 ? text : response.statusText);

  return {
    status: response.status,
    code,
    error,
    message,
  };
}

function classifyRefreshError(
  error: SupabaseAuthError,
): Exclude<RefreshGrantResult['kind'], 'success'> {
  const codeCandidates = [error.code, error.error].filter((value): value is string => !!value);
  if (codeCandidates.some((value) => INVALID_REFRESH_TOKEN_CODES.has(value.toLowerCase()))) {
    return 'invalid_refresh_token';
  }

  const searchable = codeCandidates
    .concat(error.message ?? '')
    .join(' ')
    .toLowerCase();
  if (
    /refresh[_\s-]+token.*(already[_\s-]+used|not[_\s-]+found|revoked|expired)/.test(searchable) ||
    /session.*(not[_\s-]+found|expired)/.test(searchable)
  ) {
    return 'invalid_refresh_token';
  }

  if (
    error.status === 408 ||
    error.status === 409 ||
    error.status === 429 ||
    (typeof error.status === 'number' && error.status >= 500) ||
    codeCandidates.some((value) => TRANSIENT_REFRESH_ERROR_CODES.has(value.toLowerCase()))
  ) {
    return 'transient_error';
  }

  return 'unknown_error';
}

function parseJsonObject(text: string): Record<string, unknown> | null {
  if (!text.trim()) return null;
  try {
    const parsed = JSON.parse(text) as unknown;
    return parsed && typeof parsed === 'object' && !Array.isArray(parsed)
      ? (parsed as Record<string, unknown>)
      : null;
  } catch {
    return null;
  }
}

function getStringField(obj: Record<string, unknown> | null, field: string): string | undefined {
  const value = obj?.[field];
  return typeof value === 'string' && value.length > 0 ? value : undefined;
}

function randomBytes(length: number): Uint8Array {
  const buf = new Uint8Array(length);
  crypto.getRandomValues(buf);
  return buf;
}

function base64UrlEncode(bytes: Uint8Array): string {
  let binary = '';
  for (const byte of bytes) binary += String.fromCharCode(byte);
  // btoa is available in Deno's globalThis.
  return btoa(binary).replace(/\+/g, '-').replace(/\//g, '_').replace(/=+$/, '');
}

async function sha256Base64Url(input: string): Promise<string> {
  const encoded = new TextEncoder().encode(input);
  const digest = await crypto.subtle.digest('SHA-256', encoded);
  return base64UrlEncode(new Uint8Array(digest));
}
